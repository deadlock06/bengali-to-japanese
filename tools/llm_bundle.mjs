// Builds a CHAT-SIZED, orientation-first codebase bundle for pasting/uploading
// into a chat LLM (Claude/GPT/Gemini). Unlike export_for_llm.mjs (which dumps
// EVERYTHING → ~800K tokens, too big), this curates: orientation docs + all
// Dart code + specs + tests, and EXCLUDES bulk (book content, HTML previews,
// prototypes, zips, generated/nested exports, binary-ish data).
// Run: node tools/llm_bundle.mjs   → llm_export/CODEBASE_FOR_LLM.md
import fs from 'fs';
import path from 'path';

const OUT = 'llm_export/CODEBASE_FOR_LLM.md';

// Orientation first (the LLM reads these to understand the project + state).
const ORIENTATION = [
  'CLAUDE.md', 'CODEBASE_MAP.md', 'DESIGN_BRIEF.md',
  'docs/00_START_HERE.md',
];
// Spec docs worth including (small, high-signal). Skips the biggest content ones.
const SPEC = [
  'docs/01_CONSTITUTION.md', 'docs/02_ARCHITECTURE.md', 'docs/04_AGENTS.md',
  'docs/05_CONTENT_SCHEMAS.md', 'docs/06_DATABASE.md', 'docs/07_API_SYNC.md',
  'docs/08_OFFLINE_AI.md', 'docs/09_UI_STATES.md', 'docs/11_ROADMAP_TASKS.md',
  'docs/99_DECISIONS.md', 'docs/CURRICULUM_MAP.md', 'classroom/CURRICULUM.md',
];
// Code roots (recursively include these extensions).
const CODE_DIRS = ['lib', 'test'];
const CODE_EXTS = new Set(['.dart']);
// Individual config/logic files.
const EXTRA = [
  'pubspec.yaml', 'analysis_options.yaml', 'l10n.yaml.disabled',
  'tools/lesson_batch.dart', // (already under lib; harmless if missing)
  'tools/batch_reference.mjs', 'tools/curriculum_reference.mjs',
  'tools/agents_reference.mjs', 'tools/fsrs_reference.mjs',
  'tools/validate_content.mjs',
];
// A tiny sample of content so the LLM sees the data shape (not all of it).
const CONTENT_SAMPLES = [
  'assets/curriculum/curriculum.json',
  'assets/content/lesson_greetings.json',
  'assets/content/hiragana.json',
];

const langFor = (f) => ({'.dart':'dart','.yaml':'yaml','.disabled':'yaml',
  '.json':'json','.mjs':'javascript','.md':'markdown'}[path.extname(f)] || '');

let toc = [];
let body = '';
function add(rel) {
  if (!fs.existsSync(rel) || fs.statSync(rel).isDirectory()) return;
  const txt = fs.readFileSync(rel, 'utf8');
  const anchor = rel.replace(/[^a-z0-9]/gi, '-').toLowerCase();
  toc.push(`- [\`${rel}\`](#${anchor}) — ${Math.round(txt.length/1024)}KB`);
  const md = path.extname(rel) === '.md';
  body += `\n\n<a id="${anchor}"></a>\n## \`${rel}\`\n\n`;
  body += md ? txt : '```' + langFor(rel) + '\n' + txt + '\n```\n';
}

function walk(dir) {
  const out = [];
  for (const f of fs.readdirSync(dir).sort()) {
    const p = path.join(dir, f);
    const st = fs.statSync(p);
    if (st.isDirectory()) out.push(...walk(p));
    else if (CODE_EXTS.has(path.extname(f))) out.push(p);
  }
  return out;
}

// Assemble in order: orientation → specs → code → config → content samples.
const sections = {
  '# 1. ORIENTATION (read first)': ORIENTATION,
  '# 2. SPEC (how it should be built)': SPEC,
  '# 3. CODE (lib/ + test/)': CODE_DIRS.flatMap(walk),
  '# 4. CONFIG & PROOFS': EXTRA,
  '# 5. CONTENT SAMPLES (data shape)': CONTENT_SAMPLES,
};
for (const [title, files] of Object.entries(sections)) {
  body += `\n\n${title}\n`;
  for (const f of files) add(f);
}

const header = `# SENSEI / Bhasago — codebase bundle for a chat LLM
Generated by tools/llm_bundle.mjs. Curated (code + specs + orientation);
book content, HTML previews, prototypes, zips, and generated files are EXCLUDED.
Start with section 1 (CLAUDE.md + CODEBASE_MAP.md) to understand the project and
its current build state, then section 2 for the spec, section 3 for the code.

## Table of contents
${toc.join('\n')}
`;

fs.mkdirSync('llm_export', { recursive: true });
fs.writeFileSync(OUT, header + body);
const bytes = fs.statSync(OUT).size;
console.log(`${OUT}\n${(bytes/1024).toFixed(0)} KB · ~${Math.round(bytes/4/1000)}K tokens · ${toc.length} files`);
